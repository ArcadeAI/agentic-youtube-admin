import { mkdtemp, readFile, rm } from "node:fs/promises";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { google } from "googleapis";
import OpenAI from "openai";
import type { LibraryService } from "./library.service";

interface TranscriptionResult {
	success: boolean;
	method: "captions" | "whisper" | null;
}

interface CaptionTrack {
	language: string;
	isAutoGenerated: boolean;
	trackName: string;
}

export class TranscriptionService {
	private youtube;
	private openai;

	constructor(
		private libraryService: LibraryService,
		youtubeApiKey: string,
		openaiApiKey: string,
	) {
		this.youtube = google.youtube({ version: "v3", auth: youtubeApiKey });
		this.openai = new OpenAI({ apiKey: openaiApiKey });
	}

	/**
	 * Transcribe a single video. Tries YouTube captions first, falls back to Whisper.
	 * Returns early if the video is already transcribed on disk.
	 */
	async transcribeVideo(
		channelYtId: string,
		videoId: string,
		title: string,
	): Promise<TranscriptionResult> {
		// Defensive: check if already on disk
		const existing = await this.libraryService.readTranscription(
			channelYtId,
			videoId,
			title,
		);
		if (existing) {
			return { success: true, method: null };
		}

		// Try YouTube captions first
		try {
			const captionText = await this.fetchCaptions(videoId);
			if (captionText) {
				await this.libraryService.writeTranscription(
					channelYtId,
					videoId,
					title,
					captionText,
				);
				return { success: true, method: "captions" };
			}
		} catch (err) {
			console.warn(
				`Caption fetch failed for ${videoId}, trying Whisper:`,
				err instanceof Error ? err.message : err,
			);
		}

		// Whisper fallback
		try {
			const whisperText = await this.transcribeWithWhisper(videoId);
			await this.libraryService.writeTranscription(
				channelYtId,
				videoId,
				title,
				whisperText,
			);
			return { success: true, method: "whisper" };
		} catch (err) {
			console.error(
				`Whisper transcription failed for ${videoId}:`,
				err instanceof Error ? err.message : err,
			);
			return { success: false, method: null };
		}
	}

	// ── YouTube Captions ────────────────────────────────────────────────────

	/**
	 * Fetch caption text using YouTube Data API v3 captions.list
	 * and the timedtext endpoint for download.
	 */
	private async fetchCaptions(videoId: string): Promise<string | null> {
		const tracks = await this.listCaptionTracks(videoId);
		if (tracks.length === 0) return null;

		const track = this.pickBestTrack(tracks);
		if (!track) return null;

		return this.downloadCaptionText(videoId, track.language);
	}

	private async listCaptionTracks(videoId: string): Promise<CaptionTrack[]> {
		const response = await this.youtube.captions.list({
			part: ["snippet"],
			videoId,
		});

		const items = response.data.items ?? [];
		return items.map((item) => ({
			language: item.snippet?.language ?? "en",
			isAutoGenerated: item.snippet?.trackKind === "ASR",
			trackName: item.snippet?.name ?? "",
		}));
	}

	/**
	 * Pick the best caption track:
	 * 1. Manual English
	 * 2. Auto-generated English
	 * 3. Any manual track
	 * 4. Any auto-generated track
	 */
	private pickBestTrack(tracks: CaptionTrack[]): CaptionTrack | null {
		const manualEn = tracks.find(
			(t) => t.language.startsWith("en") && !t.isAutoGenerated,
		);
		if (manualEn) return manualEn;

		const autoEn = tracks.find(
			(t) => t.language.startsWith("en") && t.isAutoGenerated,
		);
		if (autoEn) return autoEn;

		const anyManual = tracks.find((t) => !t.isAutoGenerated);
		if (anyManual) return anyManual;

		return tracks[0] ?? null;
	}

	/**
	 * Download caption text via YouTube's timedtext endpoint.
	 * captions.download requires owner OAuth, so we use the public XML endpoint.
	 */
	private async downloadCaptionText(
		videoId: string,
		lang: string,
	): Promise<string | null> {
		const url = `https://www.youtube.com/api/timedtext?v=${videoId}&lang=${lang}&fmt=srv3`;
		const response = await fetch(url);

		if (!response.ok) return null;

		const xml = await response.text();
		if (!xml || xml.length < 10) return null;

		return this.parseTimedTextXml(xml);
	}

	/**
	 * Parse YouTube srv3 timed text XML into plain text.
	 * Format: <transcript><text start="..." dur="...">content</text>...</transcript>
	 */
	private parseTimedTextXml(xml: string): string {
		const segments: string[] = [];
		const regex = /<text[^>]*>([\s\S]*?)<\/text>/g;
		let match = regex.exec(xml);

		while (match) {
			const text = match[1]
				?.replace(/&amp;/g, "&")
				.replace(/&lt;/g, "<")
				.replace(/&gt;/g, ">")
				.replace(/&quot;/g, '"')
				.replace(/&#39;/g, "'")
				.replace(/\n/g, " ")
				.trim();

			if (text) segments.push(text);
			match = regex.exec(xml);
		}

		return segments.join(" ");
	}

	// ── Whisper Fallback ────────────────────────────────────────────────────

	/**
	 * Download audio with yt-dlp and transcribe with OpenAI Whisper.
	 */
	private async transcribeWithWhisper(videoId: string): Promise<string> {
		const tempDir = await mkdtemp(join(tmpdir(), "yt-transcribe-"));
		const audioPath = join(tempDir, `${videoId}.mp3`);

		try {
			await this.downloadAudio(videoId, audioPath);
			const audioFile = await readFile(audioPath);

			const file = new File([audioFile], `${videoId}.mp3`, {
				type: "audio/mpeg",
			});
			const result = await this.openai.audio.transcriptions.create({
				model: "whisper-1",
				file,
			});

			return result.text;
		} finally {
			await rm(tempDir, { recursive: true, force: true }).catch(() => {});
		}
	}

	private async downloadAudio(
		videoId: string,
		outputPath: string,
	): Promise<void> {
		const proc = Bun.spawn(
			[
				"yt-dlp",
				"-x",
				"--audio-format",
				"mp3",
				"--audio-quality",
				"5",
				"-o",
				outputPath,
				"--no-playlist",
				"--",
				videoId,
			],
			{ stdout: "pipe", stderr: "pipe" },
		);

		const exitCode = await proc.exited;
		if (exitCode !== 0) {
			const stderr = await new Response(proc.stderr).text();
			throw new Error(`yt-dlp failed (exit ${exitCode}): ${stderr}`);
		}
	}
}
